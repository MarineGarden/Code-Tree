package data.long_;

public class TwoLongs {
	
	private final long first;
	private final long second;

	public TwoLongs( long first , long second ) {

		this.first = first;
		this.second = second;
	
	}

	public long getFirst() {

		return first;
		
	}

	public long getSecond() {

		return second;
		
	}
	
}
